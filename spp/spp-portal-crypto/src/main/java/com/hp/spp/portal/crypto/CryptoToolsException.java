package com.hp.spp.portal.crypto;

public class CryptoToolsException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = 6676732734377101488L;

	public CryptoToolsException() {
	}

	public CryptoToolsException(String message) {
		super(message);
	}

	public CryptoToolsException(Throwable cause) {
		super(cause);
	}

	public CryptoToolsException(String message, Throwable cause) {
		super(message, cause);
	}

}
