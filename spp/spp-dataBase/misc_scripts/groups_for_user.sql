---------------------------------------------------------------------------------------
---------------------------------------------------------------------------------------
-- Description: Displays the user groups to which a user belongs
---------------------------------------------------------------------------------------
---------------------------------------------------------------------------------------
-- Usage: Change u.user_name to your user's username.
---------------------------------------------------------------------------------------
---------------------------------------------------------------------------------------
select ug.name 
from usergroups ug, 
usergroupmembership ugm, 
users u 
where ug.unique_id = ugm.group_uid
and ugm.user_uid = u.unique_id
and u.user_name = 'sppuser'