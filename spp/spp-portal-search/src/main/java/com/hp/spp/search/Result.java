package com.hp.spp.search;
public class Result {
	private String urlAttribute;
	private String title;
	private String navigationItemId;
	private String siteName;

	/**
	 * @return Returns the urlAttribute.
	 */
	public String getUrlAttribute() {
		return urlAttribute;
	}
	/**
	 * @param urlAttribute The urlAttribute to set.
	 */
	public void setUrlAttribute(String urlAttribute) {
		this.urlAttribute = urlAttribute;
	}

	/**
	 * @return Returns the title.
	 */
	public String getTitle() {
		return title;
	}
	/**
	 * @param title The title to set.
	 */
	public void setTitle(String title) {
		this.title = title;
	}
	/**
	 * @return Returns the navigationItemId.
	 */
	public String getNavigationItemId() {
		return navigationItemId;
	}
	/**
	 * @param navigationItemId The navigationItemId to set.
	 */
	public void setNavigationItemId(String navigationItemId) {
		this.navigationItemId = navigationItemId;
	}

	/**
	 * @return Returns the siteName.
	 */
	public String getSiteName() {
		return siteName;
	}
	/**
	 * @param siteName The siteName to set.
	 */
	public void setSiteName(String siteName) {
		this.siteName = siteName;
	}

}
