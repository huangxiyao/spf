package com.hp.spp.search.audience.service;

public class ItemIndexingServiceException extends Exception {

	public ItemIndexingServiceException() {
		super();		
	}
 
	public ItemIndexingServiceException(String arg0) {
		super(arg0);		
	}

	public ItemIndexingServiceException(String arg0, Throwable arg1) {
		super(arg0, arg1);		
	}

	public ItemIndexingServiceException(Throwable arg0) {
		super(arg0);		
	}

}