package com.hp.spp.portal.simulate.bean;

public class SimulateBeanException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = -7442775390711367148L;

	public SimulateBeanException() {
	}

	public SimulateBeanException(String message) {
		super(message);
	}

	public SimulateBeanException(Throwable cause) {
		super(cause);
	}

	public SimulateBeanException(String message, Throwable cause) {
		super(message, cause);
	}

}
